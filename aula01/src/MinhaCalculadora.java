public class MinhaCalculadora {
    public int subtr(int num1, int num2){
        return num1 - num2;
    }
    public int soma(int num1, int num2){
        return num1 + num2;
    }
    public int multi(int num1, int num2){
        return num1 * num2;
    }
    public int div(int num1, int num2){
        return num1 / num2;
    }
}
